var currentQuestion;
var correctAnswer;
var incorrectAnswer;
var unanswered;
var seconds;
var time;
var answered;
var userSelect;
var messages = {
    correct: "Correct!",
    incorrect: "Wrong!",
    endTime: "Time's Up!",
    finished: "Alright, let's see how well you did."
}

$('#startBtn').on('click', function() {
    $(this).hide();
    newGame();
});

$('#startOverBtn').on('click', function() {
    $(this).hide();
    newGame();
});

function newGame() {
    $('#finalMessage').empty();
    $('#correctAnswers').empty();
    $('#incorrectAnswers').empty();
    $('#unanswered').empty();
    currentQuestion = 0;
    correctAnswer = 0;
    incorrectAnswer = 0;
    unanswered = 0;
    newQuestion();
}

function newQuestion() {
    $('#message').empty();
    $('#correctedAnswer').empty();
    $('#gif').empty();
    answered = true;

    // This sets up new questions and answerList
$('#currentQuestion').html('Question #' + (currentQuestion + 1) + '/' + triviaQuestions.length);
$('.question').html('<h2>' + triviaQuestions[currentQuestion].question + '</h2>');
for(var i = 0; i < 4; i++) {
    var choices = $('<div>');
    choices.text(triviaQuestions[currentQuestion].answerList[i]);
    choices.attr({'data-index': i});
    choices.addClass('thisChoice');
    $('.answerList').append(choices);
}
countdown();
// Clicking an answer will pause the time and set up answerPage
$('.thisChoice').on('click', function() {
    userSelect = $(this).data('index');
    clearInterval(time);
    answerPage();
});
}

function countdown() {
    seconds = 15;
    $('#timeLeft').html('<h3>Time Remianing: ' + seconds + '</h3>');
    answered = true;
    // This sets the time to go down
    time = setInterval(showCountdown, 1000);
}

function showCountdown() {
    seconds--;
    $('#timeLeft').html('<h3>Time Remaining: ' + seconds + '</h3>');
    if(seconds < 1) {
        clearInterval(time);
        answered = false;
        answerPage();
    }
}

function answerPage() {
    $('#currentQuestion').empty();
    $('.thisChoice').empty(); // This clears question page
    $('.question').empty();

    var rightAnswerText = triviaQuestions[currentQuestion].answerList[triviaQuestions[currentQuestion].answer];
    var rightAnswerIndex = triviaQuestions[currentQuestion].answer;
    $('#gif').html('<img src = "assets/images/' + gifArray[currentQuestion] + '.gif" width = "400px">');

    // This checks to see if correct, incorrect, or unanswered
    if((userSelect == rightAnswerIndex) && (answered == true)) {
        correctAnswer++;
        $('#message').html(messages.correct);
    } else if((userSelect != rightAnswerIndex) && (answered == true)) {
        incorrectAnswer++;
        $('#message').html(messages.incorrect);
        $('#correctedAnswer').html('The correct answer was: ' + rightAnswerText);        
    }else{
        unanswered++;
        $('#message').html(messages.endTime);
        $('#correctedAnswer').html('The correct answer was: ' + rightAnswerText);
        answered = true;
    }

    if(currentQuestion == (triviaQuestions.length-1)) {
        setTimeout(scoreboard, 5000)
    }else{
        currentQuestion++;
        setTimeout(newQuestion, 5000);
    }
}

function scoreboard() {
    $('#timeLeft').empty();
    $('#message').empty();
    $('#correctedAnswer').empty();
    $('#gif').empty();
    $('#finalMessage').html(messages.finished);
    $('#correctAnswers').html("Correct Answers: " + correctAnswer);
    $('#incorrectAnswers').html("Incorrect Answers: " + incorrectAnswer);
    $('#unanswered').html("Unanswered: " + unanswered);
    $('#startOverBtn').addClass('reset');
    $('#startOverBtn').show();
    $('#startOverBtn').html('Start Over?');
}



